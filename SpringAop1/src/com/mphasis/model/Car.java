package com.mphasis.model;

public class Car {

	String engine;
	double weight;
	public Car(String engine, double weight) {
		super();
		this.engine = engine;
		this.weight = weight;
	}
	public String getEngine() {
		return engine;
	}
	public void setEngine(String engine) {
		this.engine = engine;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	//Comment  Testing
}
